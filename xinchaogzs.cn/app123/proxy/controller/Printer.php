    public function price_all()
    {
        if (request()->isPost()) {
            $post = input('post.');
            if (!empty($post['shop_id'])) {
                $print_id = Db::table('do_printer')->where('shop_id',$post['shop_id'])->column('id');
                if (count($print_id)>0){
                    $insert = [];
                    foreach ($post['type'] as $type => $price) {
                        $insert[] = [
                            'type' => $type,
                            'price' => bcmul($price, '100', 0),
                        ];
                    }
                    $res = 0;

                    $print_id_all = implode(',',$print_id);
                    Db::table('do_printer_price')
                        ->where('printer_id', 'in', $print_id_all)
                        ->delete();
                    foreach ($print_id as $i => $id) {
