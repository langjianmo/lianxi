    public function index()
    {

        $where = "";
        $key = input("param.key", "", "trim");
        $page = input("param.page", "", "trim");
        if ($key) {
            $where = "a.status <> 99 and (a.name like '%$key%' or b.name like '%$key%' or b.shop_name like '%$key%')";
        } else {
            $where = "a.status <> 99";
        }

        $or_where = [
            [
                "status",
                "=",
                1
            ],
            [
                "out_refund_status",
                "in",
                "null,0,2"
            ]
        ];

        $day_sql = Db::table("do_order")
            ->fieldRaw("printer_id,sum(total_fee) day_commission")
            ->where($or_where)
            ->whereDay("inserttime")
            ->group("printer_id")
            ->buildSql();

        $week_sql = Db::table("do_order")
            ->fieldRaw("printer_id,sum(total_fee) week_commission")
            ->where($or_where)
            ->whereWeek("inserttime")
            ->group("printer_id")
            ->buildSql();

        $month_sql = Db::table("do_order")
            ->fieldRaw("printer_id,sum(total_fee) month_commission")
            ->where($or_where)
            ->whereMonth("inserttime")
            ->group("printer_id")
            ->buildSql();

        $data = Db::table(self::$table)
            ->alias('a')
            ->field('a.*,b.shop_name shop_name')
            ->whereRaw($where)
            ->join('do_shop b', 'a.shop_id=b.id', 'left')
            ->where('a.proxy_id', $this->proxy['id'])
            ->leftJoin($day_sql . " d", "a.id=d.printer_id")
            ->leftJoin($week_sql . " e", "a.id=e.printer_id")
            ->leftJoin($month_sql . " f", "a.id=f.printer_id")
            ->order('id')
            ->paginate();

        if (!empty($data)) {
            foreach ($data as $k => $v) {
                if ($v['functions']) {
                    $v['functions'] = json_decode($v['functions'], true);
                } else {
                    $v['functions'] = [];
                }
                $data[$k] = $v;
            }
        }

        return view('', ['lists' => $data,'page' => $page, 'pages' => $data->render(),"key"=>$key]);
    }

