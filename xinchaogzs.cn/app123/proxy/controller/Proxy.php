<?php
declare (strict_types=1);

namespace app\proxy\controller;

use think\facade\Db;
use think\facade\View;

class Proxy extends Common
{
    public function initialize()
    {
        parent::initialize(); // TODO: Change the autogenerated stub
    }

    public function index()
    {

        if (request()->isPost()) {

            $data = input("post.");
            Db::table("do_proxy")->where(["id" => $data["id"]])->update($data);
            $return = [
                "code" => 0,
                "msg" => "操作成功"
            ];
            $shop = Db::table("do_proxy")->find($data["id"]);
            session("shop", $shop);
            return json($return);
        } else {


            View::assign($this->proxy);

            return view();
        }
    }
}
